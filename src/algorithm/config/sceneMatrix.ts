/**
 * 查询照片场景与歌曲场景标签的关系值
 * @param photoScene 照片场景(含衍生 travel)
 * @param songSceneTag 歌曲场景标签
 * @returns 关系值 [0,1],无匹配时返回 0.5(中性)
 */
export function getSceneRelation(
  photoScene: ExtendedSceneType,
  songSceneTag: SongSceneTag,
): number {
  return SCENE_RELATION_MATRIX[photoScene]?.[songSceneTag] ?? 0.5;
}

/**
 * 计算照片场景与歌曲场景标签集合的最大匹配度
 * score_scene = max_{s_s ∈ S_s} rel(s_p, s_s)
 * 若歌曲无场景标签,返回 0.5(中性)
 */
export function calcSceneScore(
  photoScene: ExtendedSceneType,
  songSceneTags: readonly SongSceneTag[],
): number {
  if (songSceneTags.length === 0) return 0.5;
  let max = 0;
  for (const tag of songSceneTags) {
    const rel = getSceneRelation(photoScene, tag);
    if (rel > max) max = rel;
  }
  return max;
}

